package com.kofu.infra.modules.airmembers;

public class AirMembers {
	private String memberSeq;
	private String user_dob;
	private String user_email;
	private String user_signupDate;
	private String user_id;
	private Integer user_gender;
	private String user_favoriteLanguage;
	private String user_name;
	
	
	
	public String getMemberSeq() {
		return memberSeq;
	}
	public void setMemberSeq(String memberSeq) {
		this.memberSeq = memberSeq;
	}
	public String getUser_dob() {
		return user_dob;
	}
	public void setUser_dob(String user_dob) {
		this.user_dob = user_dob;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	public String getUser_signupDate() {
		return user_signupDate;
	}
	public void setUser_signupDate(String user_signupDate) {
		this.user_signupDate = user_signupDate;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public Integer getUser_gender() {
		return user_gender;
	}
	public void setUser_gender(Integer user_gender) {
		this.user_gender = user_gender;
	}
	public String getUser_favoriteLanguage() {
		return user_favoriteLanguage;
	}
	public void setUser_favoriteLanguage(String user_favoriteLanguage) {
		this.user_favoriteLanguage = user_favoriteLanguage;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	
	
}
